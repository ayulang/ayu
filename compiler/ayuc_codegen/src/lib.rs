mod export;

use std::collections::VecDeque;

use ayuc_hir::{
    AlternateBranch, AssignOp, AssignStmt, BinaryOp, Block, Def, Expr, ExprKind, ExternFnItem,
    ExternModItem, FnItem, IfStmt, InlineModItem, IntlSegment, Item, ItemKind, LetStmt, Literal,
    Parameter, PatKind, Path, Stmt, StmtKind, Visibility,
};
use ayuc_id::hir::DefId;
use ayuc_lower::LoweringContext;
use ayuc_pretty::{doc::Doc, renderer::Renderer};
use ayuc_resolve::resolver::ResolutionContext;
use ayuc_session::Session;
use ayuc_span::symbol::Symbol;

use crate::export::Export;

pub struct LuauCodegen<'a> {
    rcx: &'a ResolutionContext,
    lcx: &'a LoweringContext,
    sess: &'a Session,
}

impl<'a> LuauCodegen<'a> {
    pub fn new(rcx: &'a ResolutionContext, lcx: &'a LoweringContext, sess: &'a Session) -> Self {
        Self { rcx, lcx, sess }
    }

    pub fn emit(rcx: &'a ResolutionContext, lcx: &'a LoweringContext, sess: &'a Session) -> String {
        let this = Self::new(rcx, lcx, sess);

        this.generate_code()
    }

    pub fn generate_code(&self) -> String {
        let mut doc = self.to_doc();
        let mut contains_main = false;

        for (_, item) in self.lcx.top_items() {
            if let ItemKind::Fn(FnItem { name, .. }) = item.kind
                && name.as_str() == "main"
            {
                contains_main = true;
            }
        }

        if contains_main {
            doc = Doc::Concat(vec![
                doc,
                Doc::text("return main() -- autogenerated by ayuc"),
            ])
        } else {
            let exports = self.defs_to_exports(&self.lcx.top_level_items, &[]);

            let parts = if exports.is_empty() {
                Doc::Skip
            } else {
                let docs = exports
                    .into_iter()
                    .map(|e| Self::export_to_doc(e))
                    .enumerate()
                    .map(|(i, doc)| {
                        if i == 0 {
                            doc
                        } else {
                            Doc::concat([Doc::text(","), Doc::Hardline, doc])
                        }
                    })
                    .collect::<Vec<_>>();

                Doc::concat([Doc::Hardline, Doc::indent(Doc::concat(docs)), Doc::Hardline])
            };

            doc = Doc::Concat(vec![
                doc,
                Doc::Concat(vec![Doc::text("return {"), parts, Doc::text("}")]),
            ]);
        }

        Renderer::new().render(&doc)
    }

    fn defs_to_exports(&self, defs: &[DefId], absolute_path: &[&'a str]) -> Vec<Export<'a>> {
        defs.iter()
            .map(|def| &self.lcx.items[*def])
            .filter(|i| i.vis == Visibility::Public)
            .flat_map(|i| match &i.kind {
                ItemKind::ExternFn(_) | ItemKind::ExternMod(_) => None,
                ItemKind::Fn(fun) => Some(Export::mapped(
                    fun.name.as_str(),
                    format!(
                        "{}{}{}",
                        absolute_path.join("."),
                        if absolute_path.is_empty() { "" } else { "." },
                        fun.name
                    ),
                )),
                ItemKind::InlineMod(modu) => Some(Export::module(
                    modu.name.as_str(),
                    self.defs_to_exports(
                        &modu.items,
                        &[absolute_path, &[modu.name.as_str()]].concat(),
                    ),
                )),
            })
            .collect()
    }

    fn export_to_doc(export: Export) -> Doc {
        match export {
            Export::Mapped { name, absolute } => {
                Doc::concat([Doc::text(name), Doc::text(" = "), Doc::text(&absolute)])
            }
            Export::Module { name, children } => {
                let line = if !children.is_empty() {
                    Doc::Hardline
                } else {
                    Doc::Skip
                };

                Doc::concat([
                    Doc::text(name),
                    Doc::text(" = "),
                    Doc::text("{"),
                    line.clone(),
                    Doc::indent(Doc::Concat(
                        children
                            .into_iter()
                            .map(|export| Self::export_to_doc(export))
                            .enumerate()
                            .map(|(i, doc)| {
                                if i == 0 {
                                    doc
                                } else {
                                    Doc::concat([Doc::text(","), Doc::Hardline, doc])
                                }
                            })
                            .collect(),
                    )),
                    line,
                    Doc::text("}"),
                ])
            }
        }
    }

    fn sym_of_item(item: &Item) -> Symbol {
        match &item.kind {
            ItemKind::Fn(FnItem { name: sym, .. })
            | ItemKind::ExternFn(ExternFnItem {
                name: sym,
                ffi_name: None,
                ..
            })
            | ItemKind::ExternFn(ExternFnItem {
                name: _,
                ffi_name: Some(sym),
                ..
            })
            | ItemKind::InlineMod(InlineModItem { name: sym, .. })
            | ItemKind::ExternMod(ExternModItem {
                name: sym,
                ffi_name: None,
                ..
            })
            | ItemKind::ExternMod(ExternModItem {
                name: _,
                ffi_name: Some(sym),
                ..
            }) => *sym,
        }
    }

    fn to_doc(&self) -> Doc {
        // 1st pass: Declare all items
        let first = Doc::Concat(
            self.lcx
                .top_items()
                .iter()
                .flat_map(|(_, item)| self.declare_item(item, false))
                .map(|doc| Doc::concat([doc, Doc::Hardline]))
                .collect(),
        );

        // 2nd pass: Define all items
        let second = Doc::Concat(
            self.lcx
                .top_items()
                .iter()
                .flat_map(|(_, item)| self.define_item(item, &[]))
                .map(|doc| Doc::Concat(vec![doc, Doc::Hardline, Doc::Blankline]))
                .collect(),
        );

        Doc::concat([first, Doc::Blankline, second])
    }

    fn is_visible_item(&self, item: &Item) -> bool {
        match &item.kind {
            ItemKind::ExternFn(_) | ItemKind::ExternMod(_) => false,
            ItemKind::Fn(_) => true,
            ItemKind::InlineMod(decl) => decl
                .items
                .iter()
                .map(|id| &self.lcx.items[*id])
                .any(|item| self.is_visible_item(item)),
        }
    }

    fn declare_item(&self, item: &Item, within_module: bool) -> Option<Doc> {
        match &item.kind {
            ItemKind::InlineMod(decl) => {
                let has_visible_items = self.is_visible_item(item);

                if !has_visible_items {
                    return None;
                }

                let children = decl
                    .items
                    .iter()
                    .map(|id| &self.lcx.items[*id])
                    .flat_map(|item| self.declare_item(item, true))
                    .collect::<Vec<_>>();

                Some(Doc::concat([
                    if !within_module {
                        Doc::text("local ")
                    } else {
                        Doc::Skip
                    },
                    Doc::text(format!("{} = ", decl.name)),
                    Doc::text("{"),
                    if children.is_empty() {
                        Doc::Skip
                    } else {
                        Doc::concat([
                            Doc::Hardline,
                            Doc::indent(Doc::concat(children)),
                            Doc::Hardline,
                        ])
                    },
                    Doc::text("}"),
                ]))
            }
            ItemKind::Fn(decl) => {
                (!within_module).then_some(Doc::text(format!("local {}", decl.name)))
            }
            ItemKind::ExternMod(_) | ItemKind::ExternFn(_) => None,
        }
    }

    fn define_item(&self, item: &Item, absolute_path: &[Symbol]) -> Option<Doc> {
        match &item.kind {
            ItemKind::InlineMod(decl) => {
                let items = decl
                    .items
                    .iter()
                    .flat_map(|item| {
                        self.define_item(
                            &self.lcx.items[*item],
                            &[absolute_path, &[decl.name]].concat(),
                        )
                    })
                    .enumerate()
                    .map(|(i, doc)| {
                        if i != 0 {
                            Doc::concat([Doc::Hardline, Doc::Blankline, doc])
                        } else {
                            doc
                        }
                    })
                    .collect::<Vec<_>>();

                if items.is_empty() {
                    None
                } else {
                    Some(Doc::Concat(items))
                }
            }
            ItemKind::Fn(decl) => {
                let mut params = Vec::new();

                for (i, param) in decl.params.iter().enumerate() {
                    if i != 0 {
                        params.push(Doc::text(", "));
                    }

                    params.push(self.param_to_doc(param));
                }

                Some(Doc::Concat(Vec::from([
                    Self::syms_to_doc(&[absolute_path, &[decl.name]].concat()),
                    Doc::text(" = "),
                    Doc::text("function"),
                    Doc::text("("),
                    Doc::Concat(params),
                    Doc::text(")"),
                    Doc::Hardline,
                    Doc::indent(self.block_to_doc(&decl.block)),
                    Doc::Hardline,
                    Doc::text("end"),
                ])))
            }
            ItemKind::ExternFn(_) | ItemKind::ExternMod(_) => None,
        }
    }

    fn syms_to_doc(syms: &[Symbol]) -> Doc {
        Doc::Concat(
            syms.iter()
                .enumerate()
                .map(|(i, sym)| {
                    if i != 0 {
                        Doc::concat([Doc::text("."), Doc::text(sym.as_str())])
                    } else {
                        Doc::text(sym.as_str())
                    }
                })
                .collect(),
        )
    }

    fn param_to_doc(&self, param: &Parameter) -> Doc {
        Doc::text(param.name.as_str())
    }

    fn block_to_doc(&self, block: &Block) -> Doc {
        let mut docs = Vec::new();

        for (i, stmt) in block.stmts.iter().enumerate() {
            if i != 0 {
                docs.push(Doc::StmtSep);
            }

            docs.push(self.stmt_to_doc(stmt));
        }

        Doc::Concat(docs)
    }

    fn stmt_to_doc(&self, stmt: &Stmt) -> Doc {
        match &stmt.kind {
            StmtKind::Break => Doc::text("break"),
            StmtKind::Loop(r#loop) => Doc::Concat(Vec::from([
                Doc::text("while true do"),
                Doc::Hardline,
                Doc::indent(self.block_to_doc(&r#loop.block)),
                Doc::Hardline,
                Doc::text("end"),
            ])),
            StmtKind::While(r#while) => Doc::Concat(Vec::from([
                Doc::text("while "),
                self.expr_to_doc(&r#while.expr, false),
                Doc::text(" do"),
                Doc::Hardline,
                Doc::indent(self.block_to_doc(&r#while.block)),
                Doc::Hardline,
                Doc::text("end"),
            ])),
            StmtKind::Assign(assign) => self.assign_stmt_to_doc(assign),
            StmtKind::Return(ret) => Doc::Concat(vec![
                Doc::text("return "),
                self.expr_to_doc(&ret.expr, false),
            ]),
            StmtKind::Expr(expr) => self.expr_to_doc(expr, false),
            StmtKind::If(if_stmt) => self.if_stmt_to_doc(if_stmt, false),
            StmtKind::Let(let_stmt) => self.let_stmt_to_doc(let_stmt),
        }
    }

    fn assign_stmt_to_doc(&self, assign_stmt: &AssignStmt) -> Doc {
        let node_id = self
            .lcx
            .id_mappings
            .get_by_right(&assign_stmt.value.id)
            .copied()
            .unwrap();
        let ty_res = self.rcx.ty_of(node_id);
        let wrap_expr = if let ayuc_resolve::TyKind::Tuple(inner) = &ty_res.kind
            && inner.len() > 1
        {
            matches!(
                assign_stmt.value.kind,
                ExprKind::Call(_) | ExprKind::Tuple(_)
            )
        } else {
            false
        };
        let expr_doc = self.expr_to_doc(&assign_stmt.value, false);

        Doc::Concat(Vec::from([
            Doc::text(self.def_to_sym(&assign_stmt.ident).as_str()),
            Doc::text(" "),
            Doc::text(match assign_stmt.op {
                AssignOp::Add => "+=",
                AssignOp::Sub => "-=",
                AssignOp::Assign => "=",
                AssignOp::Div => "/=",
                AssignOp::Modulus => "%=",
                AssignOp::Mul => "*=",
            }),
            Doc::text(" "),
            if wrap_expr {
                Doc::concat([Doc::text("{"), expr_doc, Doc::text("}")])
            } else {
                expr_doc
            },
        ]))
    }

    fn let_stmt_to_doc(&self, let_stmt: &LetStmt) -> Doc {
        let expr_doc = self.expr_to_doc(&let_stmt.init, false);

        match &let_stmt.pat.kind {
            PatKind::Identifier { sym, .. } => {
                let node_id = self
                    .lcx
                    .id_mappings
                    .get_by_right(&let_stmt.init.id)
                    .copied()
                    .unwrap();
                let ty_res = self.rcx.ty_of(node_id);
                let wrap_expr = if let ayuc_resolve::TyKind::Tuple(inner) = &ty_res.kind
                    && inner.len() > 1
                {
                    matches!(let_stmt.init.kind, ExprKind::Call(_) | ExprKind::Tuple(_))
                } else {
                    false
                };

                Doc::concat([
                    Doc::text("local "),
                    Doc::text(sym.as_str()),
                    if expr_doc.is_empty() {
                        Doc::Skip
                    } else {
                        Doc::concat([
                            Doc::text(" = "),
                            if wrap_expr {
                                Doc::concat([Doc::text("{"), expr_doc, Doc::text("}")])
                            } else {
                                expr_doc
                            },
                        ])
                    },
                ])
            }
            PatKind::Tuple(parts) => {
                let expr_needs_unpacking =
                    !matches!(let_stmt.init.kind, ExprKind::Call(_) | ExprKind::Tuple(_));
                let has_nested_tuples = parts
                    .iter()
                    .any(|part| matches!(part.kind, PatKind::Tuple(_)));

                if has_nested_tuples || expr_needs_unpacking {
                    let mut temps = Vec::new();
                    let mut assignments = Vec::new();

                    for (idx, pat) in parts.iter().enumerate() {
                        match &pat.kind {
                            PatKind::Identifier { sym, .. } => assignments.push(sym.to_string()),
                            PatKind::Tuple(_) => {
                                let temp_name = format!("tmp_{}", idx);

                                temps.push(temp_name.clone());
                                assignments.push(temp_name);
                            }
                        }
                    }

                    let mut collected_identifiers = Vec::new();
                    let mut collected_assignments = Vec::new();

                    let mut pats_left = VecDeque::from_iter(
                        parts
                            .iter()
                            .enumerate()
                            .map(|(i, pat)| (i, pat, Vec::<usize>::new())),
                    );

                    while let Some((i, pat, path)) = pats_left.pop_front() {
                        match &pat.kind {
                            PatKind::Identifier { sym, .. } => {
                                collected_identifiers.push(*sym);

                                if path.is_empty() {
                                    continue;
                                }

                                let path = &[path.as_slice(), &[i]].concat();
                                let top_level = &assignments[path[0]];
                                let indexers = &path[1..];

                                collected_assignments.push(Doc::concat([
                                    Doc::text(sym.as_str()),
                                    Doc::text(" = "),
                                    Doc::concat([
                                        Doc::text(top_level),
                                        Doc::concat(indexers.iter().map(|i| {
                                            Doc::concat([
                                                Doc::text("["),
                                                Doc::text(format!("{}", i + 1)),
                                                Doc::text("]"),
                                            ])
                                        })),
                                    ]),
                                ]));
                            }
                            PatKind::Tuple(parts) => {
                                pats_left.extend(parts.iter().enumerate().map(|(i2, pat)| {
                                    (
                                        i2,
                                        pat,
                                        path.iter().copied().chain(std::iter::once(i)).collect(),
                                    )
                                }))
                            }
                        }
                    }

                    let tmp_decls = if temps.is_empty() {
                        Doc::Skip
                    } else {
                        Doc::concat([
                            Doc::text("local "),
                            Doc::separated(temps.iter().map(Doc::text), Doc::text(", ")),
                            Doc::Hardline,
                        ])
                    };

                    let assignments = Doc::concat([
                        Doc::text("do"),
                        Doc::Hardline,
                        Doc::indent(Doc::concat([
                            tmp_decls,
                            Doc::concat([
                                Doc::separated(assignments.iter().map(Doc::text), Doc::text(", ")),
                                Doc::text(" = "),
                                if expr_needs_unpacking {
                                    Doc::separated(
                                        (0..assignments.len()).into_iter().map(|i| {
                                            Doc::concat([
                                                expr_doc.clone(),
                                                Doc::text(format!("[{}]", i + 1)),
                                            ])
                                        }),
                                        Doc::text(", "),
                                    )
                                } else {
                                    expr_doc
                                },
                            ]),
                            if collected_assignments.is_empty() {
                                Doc::Skip
                            } else {
                                Doc::concat([
                                    Doc::Hardline,
                                    Doc::separated(collected_assignments, Doc::StmtSep),
                                ])
                            },
                        ])),
                        Doc::Hardline,
                        Doc::text("end"),
                    ]);

                    Doc::concat([
                        Doc::text("local "),
                        Doc::separated(
                            collected_identifiers
                                .iter()
                                .map(|sym| Doc::text(sym.as_str())),
                            Doc::text(", "),
                        ),
                        Doc::Hardline,
                        assignments,
                    ])
                } else {
                    Doc::concat([
                        Doc::text("local "),
                        Doc::separated(
                            parts.iter().map(|part| match &part.kind {
                                PatKind::Identifier { sym, .. } => Doc::text(sym.as_str()),
                                _ => unreachable!(),
                            }),
                            Doc::text(", "),
                        ),
                        Doc::text(" = "),
                        expr_doc,
                    ])
                }
            }
        }
    }

    fn if_stmt_to_doc(&self, if_stmt: &IfStmt, loose_end: bool) -> Doc {
        let IfStmt {
            expr,
            block,
            alternate,
        } = if_stmt;

        let mut parts = Vec::from([
            Doc::text("if "),
            self.expr_to_doc(expr, false),
            Doc::text(" then"),
            Doc::Hardline,
            Doc::indent(self.block_to_doc(block)),
            Doc::Hardline,
        ]);

        match alternate {
            Some(AlternateBranch::Another(if_stmt)) => {
                parts.extend([Doc::text("else"), self.if_stmt_to_doc(if_stmt, true)]);
            }
            Some(AlternateBranch::Final(block)) => {
                parts.extend([
                    Doc::text("else"),
                    Doc::Hardline,
                    Doc::indent(self.block_to_doc(block)),
                    Doc::Hardline,
                ]);
            }
            None => {}
        }

        if !loose_end {
            parts.push(Doc::text("end"));
        }

        Doc::Concat(parts)
    }

    fn expr_to_doc(&self, expr: &Expr, wrap_expr: bool) -> Doc {
        match &expr.kind {
            ExprKind::Tuple(inner) => {
                let all = Doc::Concat(
                    inner
                        .iter()
                        .map(|expr| self.expr_to_doc(expr, true))
                        .enumerate()
                        .map(|(i, doc)| {
                            if i == 0 {
                                doc
                            } else {
                                Doc::concat([Doc::text(", "), doc])
                            }
                        })
                        .collect(),
                );

                if wrap_expr {
                    // This only applies to tuples that are inside other expressions.
                    // Lua doesn't allow ((number, number), number), so we convert it to ({number}, number).

                    Doc::concat([Doc::text("{"), all, Doc::text("}")])
                } else {
                    all
                }
            }
            ExprKind::Lit(lit) => match lit {
                Literal::Bool(value) => Doc::text(if *value { "true" } else { "false" }),
                Literal::Integer(value) => Doc::text(value.to_string()),
                Literal::Str(str) => Doc::Concat(Vec::from([
                    Doc::text("\""),
                    Doc::text(str.as_str()),
                    Doc::text("\""),
                ])),
                Literal::InterpolatedStr(segments) => Doc::Concat(Vec::from([
                    Doc::text("`"),
                    Doc::Concat(
                        segments
                            .iter()
                            .map(|segment| match segment {
                                IntlSegment::Text(text) => Doc::text(
                                    text.as_str().replace("{{", "\\{").replace("}}", "\\}"),
                                ),
                                IntlSegment::Var(def) => Doc::Concat(Vec::from([
                                    Doc::text("{"),
                                    Doc::text(self.def_to_sym(def).as_str()),
                                    Doc::text("}"),
                                ])),
                            })
                            .collect(),
                    ),
                    Doc::text("`"),
                ])),
            },
            ExprKind::Binary(bin) => {
                let doc = Doc::Concat(Vec::from([
                    self.expr_to_doc(&bin.left, true),
                    Doc::text(" "),
                    Doc::text(match bin.operator {
                        BinaryOp::Add => "+",
                        BinaryOp::Gt => ">",
                        BinaryOp::EqualsEquals => "==",
                        BinaryOp::GtOrEqual => ">=",
                        BinaryOp::Lt => "<",
                        BinaryOp::LtOrEqual => "<=",
                        BinaryOp::Minus => "-",
                        BinaryOp::NotEquals => "~=",
                        BinaryOp::Div => "/",
                        BinaryOp::Modulus => "%",
                        BinaryOp::Mul => "*",
                    }),
                    Doc::text(" "),
                    self.expr_to_doc(&bin.right, true),
                ]));

                if wrap_expr {
                    Doc::concat([Doc::text("("), doc, Doc::text(")")])
                } else {
                    doc
                }
            }
            ExprKind::Call(call) => Doc::Concat(Vec::from([
                self.expr_to_doc(&call.callee, true),
                Doc::text("("),
                Doc::Concat(
                    call.args
                        .iter()
                        .enumerate()
                        .flat_map(|(i, expr)| {
                            if i != 0 {
                                Vec::from([Doc::text(", "), self.expr_to_doc(expr, false)])
                            } else {
                                Vec::from([self.expr_to_doc(expr, false)])
                            }
                        })
                        .collect(),
                ),
                Doc::text(")"),
            ])),
            ExprKind::Parenthesized(expr) => Doc::concat([
                Doc::text("("),
                self.expr_to_doc(expr, false),
                Doc::text(")"),
            ]),
            ExprKind::Path(path) => self.path_to_doc(path),
        }
    }

    fn path_to_doc(&self, path: &Path) -> Doc {
        let target_is_extern = self.def_is_extern(&path.target);

        if path.segments.len() == 1 {
            return Doc::text(self.def_to_sym(&path.target).as_str());
        }

        Doc::Concat(
            path.segments
                .iter()
                .filter(|def| {
                    if target_is_extern {
                        self.def_is_extern(def)
                    } else {
                        true
                    }
                })
                .map(|def| self.def_to_doc(def))
                .enumerate()
                .map(|(i, doc)| {
                    if i != 0 {
                        Doc::Concat(vec![Doc::text("."), doc])
                    } else {
                        doc
                    }
                })
                .collect(),
        )
    }

    fn def_is_extern(&self, def: &Def) -> bool {
        if let Def::Def(id) = def {
            match &self.lcx.items[*id].kind {
                ItemKind::ExternFn(_) | ItemKind::ExternMod(_) => true,
                ItemKind::Fn(_) | ItemKind::InlineMod(_) => false,
            }
        } else {
            false
        }
    }

    fn def_to_sym(&self, def: &Def) -> Symbol {
        match def {
            Def::Def(def) => Self::sym_of_item(&self.lcx.items[*def]),
            Def::Local(local) => self.sess.local(*local).name,
        }
    }

    fn def_to_doc(&self, def: &Def) -> Doc {
        Doc::text(self.def_to_sym(def).as_str())
    }
}
