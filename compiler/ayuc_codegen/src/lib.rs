use ayuc_hir::{
    BinaryOp, Def, Expr, ExprKind, ExternFnItem, FnItem, ItemKind, Literal, Package, Stmt, StmtKind,
};
use ayuc_id::hir::PackageId;
use ayuc_tyctx::TyCtx;
use std::fmt::Write;

pub struct LuauCodegen;

fn write_expr(pkg: &Package, buf: &mut String, expr: &Expr) {
    match &expr.kind {
        ExprKind::Call(call) => {
            write_expr(pkg, buf, &call.callee);

            let _ = write!(buf, "(");

            for (i, x) in call.args.iter().enumerate() {
                write_expr(pkg, buf, x);

                if i != call.args.len() - 1 {
                    let _ = write!(buf, ", ");
                }
            }

            let _ = write!(buf, ")");
        }
        ExprKind::Ref(def) => {
            let ident = match def {
                Def::Def(def) => match &pkg.items[*def].kind {
                    ItemKind::Fn(FnItem { name, .. })
                    | ItemKind::ExternFn(ExternFnItem { name, .. }) => *name,
                    _ => unreachable!(),
                },
                Def::Local(local) => pkg.locals[*local].name,
                Def::Error => unreachable!(),
            };

            let _ = write!(buf, "{}", ident.as_str());
        }
        ExprKind::Lit(Literal::Str(str)) => {
            let _ = write!(buf, "\"{}\"", str.as_str());
        }
        ExprKind::Lit(Literal::Integer(value)) => {
            let _ = write!(buf, "{}", value);
        }
        ExprKind::Binary(bin) => {
            write_expr(pkg, buf, &bin.left);

            let _ = write!(
                buf,
                " {} ",
                match bin.operator {
                    BinaryOp::Add => "+",
                    BinaryOp::Gt => ">",
                    BinaryOp::EqualsEquals => "==",
                    BinaryOp::GtOrEqual => ">=",
                    BinaryOp::Lt => "<",
                    BinaryOp::LtOrEqual => "<=",
                    BinaryOp::Minus => "-",
                    BinaryOp::NotEquals => "~=",
                }
            );

            write_expr(pkg, buf, &bin.right);
        }
    }
}

fn write_stmt(pkg: &Package, buf: &mut String, stmt: &Stmt) {
    match &stmt.kind {
        StmtKind::If(if_stmt) => {
            let _ = write!(buf, "if ");

            write_expr(pkg, buf, &if_stmt.expr);

            let _ = writeln!(buf, " then");

            for stmt in &if_stmt.block.stmts {
                write_stmt(pkg, buf, stmt);
            }

            let _ = write!(buf, "end");
        }
        StmtKind::Expr(expr) => write_expr(pkg, buf, expr),
        StmtKind::Let(var_decl) => {
            let _ = write!(buf, "local {} = ", var_decl.ident.as_str());

            write_expr(pkg, buf, &var_decl.init);
        }
        StmtKind::Return(ret) => {
            let _ = write!(buf, "return");

            if let Some(expr) = &ret.expr {
                let _ = write!(buf, " ");

                write_expr(pkg, buf, expr);
            }
        }
    };

    let _ = writeln!(buf);
}

impl LuauCodegen {
    pub fn emit(module_id: PackageId, ty_ctx: &TyCtx) -> String {
        // this is an experimental version of the codegen which is not intended to be used.

        let mut buf = String::new();
        let mut contains_main = false;
        let pkg = ty_ctx.package(module_id);

        for item in pkg.items.values() {
            match &item.kind {
                ItemKind::Fn(fn_item) => {
                    if fn_item.name.as_str() == "main" {
                        contains_main = true;
                    }

                    let _ = write!(buf, "function {}(", fn_item.name.as_str());

                    for (i, param) in fn_item.params.iter().enumerate() {
                        if i > 0 {
                            let _ = write!(buf, ", {}", param.name.as_str());
                        } else {
                            let _ = write!(buf, "{}", param.name.as_str());
                        }
                    }

                    let _ = writeln!(buf, ")");

                    for stmt in &fn_item.block.stmts {
                        write_stmt(pkg, &mut buf, stmt);
                    }

                    let _ = writeln!(buf, "end");
                }
                ItemKind::ExternFn(_) => {}
                ItemKind::Dummy => panic!("dummy items are not allowed to exist"),
            }
        }

        if contains_main {
            let _ = writeln!(buf, "return main() -- autogenerated by ayuc");
        }

        buf
    }
}
